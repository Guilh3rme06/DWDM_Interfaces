class Translator {
    constructor() {
        this.currentLanguage = localStorage.getItem('selectedLanguage') || 'pt';
        this.supportedLanguages = {
            'pt': 'Português',
            'en': 'English',
            'es': 'Español',
            'fr': 'Français'
        };
    }

    init() {
        this.createLanguageSelector();
        this.translatePage();
        document.addEventListener('languageChanged', () => this.translatePage());
    }

    createLanguageSelector() {
        // Buscar el contenedor de acciones del header
        let headerActions = document.querySelector('#header-actions-desktop');
        let mobileHeaderActions = document.querySelector('#header-actions-mobile');
        
        // Se não encontrou nenhum, tenta o genérico
        if (!headerActions && !mobileHeaderActions) {
            headerActions = document.querySelector('.header-actions:not(.mobile-controls .header-actions)');
        }
        if (!headerActions && !mobileHeaderActions) {
            headerActions = document.querySelector('.header-actions');
        }
        if (!headerActions && !mobileHeaderActions) return;

        // Remover selectors antigos se existem
        document.querySelectorAll('.language-selector').forEach(sel => sel.remove());

        // Função auxiliar para criar selector
        const createSelect = () => {
            const select = document.createElement('select');
            select.className = 'language-selector';
            select.setAttribute('aria-label', 'Selecionar idioma');
            select.title = 'Escolher idioma';

            Object.entries(this.supportedLanguages).forEach(([code, name]) => {
                const option = document.createElement('option');
                option.value = code;
                option.textContent = name;
                // Seleciona a opção correta baseada no idioma atual
                if (code === this.currentLanguage) {
                    option.selected = true;
                }
                select.appendChild(option);
            });

            // Bind do this para poder usar changeLanguage
            const self = this;
            select.addEventListener('change', function(e) {
                self.changeLanguage(e.target.value);
            });
            
            return select;
        };

        // Insertar en desktop si existe
        if (headerActions) {
            const select = createSelect();
            const nightModeBtn = headerActions.querySelector('.modo-noturno');
            if (nightModeBtn) {
                headerActions.insertBefore(select, nightModeBtn);
            } else {
                headerActions.appendChild(select);
            }
        }
        
        // Insertar en mobile si existe (cria um novo instance)
        if (mobileHeaderActions && mobileHeaderActions !== headerActions) {
            const select = createSelect();
            const nightModeBtn = mobileHeaderActions.querySelector('.modo-noturno');
            if (nightModeBtn) {
                mobileHeaderActions.insertBefore(select, nightModeBtn);
            } else {
                mobileHeaderActions.appendChild(select);
            }
        }
    }

    changeLanguage(lang) {
        this.currentLanguage = lang;
        localStorage.setItem('selectedLanguage', lang);
        
        // Sincronizar todos os language selectors
        this.updateLanguageSelectors(lang);
        
        // Recarrega a página após 300ms para permitir atualização
        setTimeout(() => {
            window.location.reload();
        }, 300);
    }

    updateLanguageSelectors(lang) {
        // Atualizar todos os selectors para mostrar o idioma correto
        document.querySelectorAll('.language-selector').forEach(selector => {
            selector.value = lang;
        });
    }

    translatePage() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.getTranslation(key);
            if (translation) {
                element.textContent = translation;
            }
        });
    }

    getTranslation(key) {
        return translations[this.currentLanguage]?.[key] || translations['pt']?.[key] || key;
    }

    translateText(text) {
        // Método auxiliar para traduzir texto específico
        return this.getTranslation(text);
    }
}

// Inicializa automaticamente quando o DOM está pronto
document.addEventListener('DOMContentLoaded', () => {
    const translator = new Translator();
    translator.init();
});
